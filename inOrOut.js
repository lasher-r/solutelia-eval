searchRowsForBoundary = (rows, x) => {
  for (let i = 0; i < rows.length; i++) {
    if (rows[i][x] === 1) {
      return true;
    }
  }
  return false;
};

searchColsForBoundary = (cols) => {
  const found = cols.find((e) => e === 1);
  return found !== undefined;
};

isBounded = ([x, y]) => {
  if (map[y][x] === 1) {
    return 1;
  }

  const higherRows = map.slice(0, y);
  const lowerRows = map.slice(y);
  const leftCol = map[y].slice(0, x);
  const rightCol = map[y].slice(x);

  return (
    searchRowsForBoundary(higherRows, x) &&
    searchRowsForBoundary(lowerRows, x) &&
    searchColsForBoundary(leftCol) &&
    searchColsForBoundary(rightCol)
  ) ? 1 : 0;
};
